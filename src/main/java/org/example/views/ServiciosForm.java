    private void llenarTablaServiciosCulminados(LinkedList<ServicioCulminado> servicios){
        DefaultTableModel modeloT = (DefaultTableModel) tabla_serviciosCulminados.getModel();
        for (int i = modeloT.getRowCount() - 1; i >= 0; i--) {
            modeloT.removeRow(i);
        }
        if(servicios.isEmpty()){
            JOptionPane.showMessageDialog(this, "No hay datos para mostrar");
            return;
        }

        for (ServicioCulminado servicio: servicios) {
            String centro = servicio.getSolicitud().getCentro().getNombre();
            String taxi = servicio.getTaxi().getId();
            String recogida = servicio.getSolicitud().getDireccion();
            String destino = servicio.getSolicitud().getDestino();
            String horaInicio = String.valueOf(servicio.getSolicitud().getHora());
            String horaFin = String.valueOf(servicio.getHoraFin());
            float distancia = servicio.getSolicitud().getCantKm();

            Object[] datos = {centro, taxi, recogida, destino, horaInicio, horaFin, distancia};
            modeloT.addRow(datos);
        }
    }

