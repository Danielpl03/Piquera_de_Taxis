    private void tabla_serviciosEnEjecucionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_serviciosEnEjecucionMouseClicked
        if (evt.getClickCount() == 1) {  //Si se hizo un click
            target = (JTable) evt.getSource();
            row    = target.getSelectedRow(); // Obtener la fila

            btn_cancelarServicio.setEnabled(true);
            btn_maracarServicioTerminado.setEnabled(true);
        }
    }//GEN-LAST:event_tabla_serviciosEnEjecucionMouseClicked

    private void tabbedPanelStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_tabbedPanelStateChanged
        if (tabbedPanel.getSelectedIndex() == 0)
            llenarTablaServiciosEnEjecucion(ServiciosService.getServciosEnEjecucion());
        else
            llenarTablaServiciosCulminados(ServiciosService.getServiciosCulminados());
    }//GEN-LAST:event_tabbedPanelStateChanged

    private void btn_maracarServicioTerminadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_maracarServicioTerminadoActionPerformed
        if (row >= 0){
            ServiciosService.marcarServicioComoTerminado(ServiciosService.getServciosEnEjecucion().get(row));
        }
        row = -1;
        btn_maracarServicioTerminado.setEnabled(false);
        btn_cancelarServicio.setEnabled(false);
        llenarTablaServiciosEnEjecucion(ServiciosService.getServciosEnEjecucion());
    }//GEN-LAST:event_btn_maracarServicioTerminadoActionPerformed

